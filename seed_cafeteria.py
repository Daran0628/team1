with app.app_context():
    start = date(2026, 1, 1)
    end = date(2026, 7, 31)

    count = 0
    d = start
    while d <= end:
        exists = CafeteriaMenu.query.filter_by(menu_date=d).first()
        if not exists:
            weekday = d.weekday()
            if weekday == 2:
                text = random.choice(WEDNESDAY_SPECIAL)
            elif weekday >= 5:
                text = random.choice(WEEKEND_MENUS)
            else:
                text = random.choice(REGULAR_MENUS)
            menu = CafeteriaMenu(menu_date=d, menu_text=text, created_by=None)
            db.session.add(menu)
            count += 1
        d += timedelta(days=1)

    db.session.commit()
    print(f"{count}일치 메뉴 시드 완료 ({start} ~ {end})")
